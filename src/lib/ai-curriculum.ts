// Learning path optimization based on user goals
export const optimizeLearningPath = (userGoals: string[], timeFrame: number) => {
  const priorityTopics = {
    'career_advancement': ['machine_learning', 'deep_learning', 'tools_frameworks'],
    'research': ['advanced_concepts', 'future_trends', 'foundations'],
    'business': ['industry_applications', 'ai_agents', 'tools_frameworks'],
    'creative': ['generative_ai', 'computer_vision', 'language_ai'],
    'technical': ['deep_learning', 'tools_frameworks', 'advanced_concepts'],
    'comprehensive': ['foundations', 'machine_learning', 'deep_learning', 'language_ai']
  };

  const selectedAreas = userGoals.flatMap(goal => priorityTopics[goal as keyof typeof priorityTopics] || []);
  const uniqueAreas = [...new Set(selectedAreas)];
  
  return uniqueAreas.slice(0, timeFrame);
};

